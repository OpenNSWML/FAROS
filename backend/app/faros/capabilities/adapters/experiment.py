import json
from typing import Any, Dict, List

from app.faros.capabilities.base import BaseCapability
from app.faros.models.artifact import ArtifactRecord
from app.faros.models.capability import CapabilityResult
from app.faros.models.execution import ExecutionContext
from app.faros.models.profile import CapabilityBinding
from app.faros.models.provider import ProviderResult, ProviderTask
from app.db.engine import init_db
from app.modules.code.projects import code_project_service
from app.modules.code.storage import get_session_context
from app.modules.platform.storage import create_experiment


class ExperimentCapability(BaseCapability):
    capability_id = "experiment"
    description = "Create an LLM-domain experiment workspace by provisioning a code project and an experiment record."
    default_agent_id = "experimenter"
    default_skill_ids = ["experiment-scaffold", "artifact-packaging"]
    artifact_types = ["code_project", "experiment_record"]

    def _build_scaffold_files(self, title: str, paper_type: str, framework: str, language: str, inputs: Dict[str, Any], selected_candidate: Dict[str, Any], candidate_id: str | None, session_id: str | None) -> List[Dict[str, str]]:
        readme = f"# {title}\n\n"
        readme += "Generated by FAROS experiment capability for the LLM workflow.\n\n"
        if selected_candidate:
            readme += f"## Problem\n{selected_candidate.get('problem', '')}\n\n"
            readme += f"## Key Insight\n{selected_candidate.get('keyInsight', '')}\n\n"
        readme += "## Structure\n- `src/` runtime code\n- `configs/` experiment configuration\n- `scripts/` execution entrypoints\n- `tests/` basic smoke tests\n"
        config_json = {
            "title": title,
            "paperType": paper_type,
            "framework": framework,
            "seedQuery": inputs.get("seedQuery"),
            "selectedCandidateId": candidate_id,
            "ideaSessionId": session_id,
        }
        return [
            {"path": "README.md", "content": readme},
            {"path": "requirements.txt", "content": "fastapi\nuvicorn\npydantic\n"},
            {"path": "configs/experiment.json", "content": json.dumps(config_json, indent=2)},
            {"path": "src/main.py", "content": "def main():\n    print(\"FAROS experiment scaffold\")\n\n\nif __name__ == '__main__':\n    main()\n"},
            {"path": "src/pipeline.py", "content": "def build_experiment_plan():\n    return {\"status\": \"draft\"}\n"},
            {"path": "scripts/run.sh", "content": "#!/usr/bin/env bash\npython src/main.py\n"},
            {"path": "tests/test_smoke.py", "content": "from src.main import main\n\n\ndef test_main_runs():\n    assert main() is None\n"},
        ]

    def build_provider_task(self, context: ExecutionContext, inputs: Dict[str, Any], binding: CapabilityBinding) -> ProviderTask | None:
        if binding.provider_type != 'execution':
            return None
        selected_candidate = inputs.get("selectedCandidate") or {}
        session_id = inputs.get("ideaSessionId")
        candidate_id = inputs.get("selectedCandidateId")
        paper_type = inputs.get("paperType", "algorithm")
        title = selected_candidate.get("title") or inputs.get("seedQuery") or "FAROS Experiment"
        description = selected_candidate.get("problem") or inputs.get("notes") or "Experiment workspace generated by FAROS."
        language = inputs.get("language", "python")
        framework = inputs.get("framework", "FastAPI")
        return ProviderTask(
            capability_id=self.capability_id,
            provider=binding.provider,
            model=binding.model,
            options={
                'title': title,
                'description': description,
                'language': language,
                'framework': framework,
                'paperType': paper_type,
                'files': self._build_scaffold_files(title, paper_type, framework, language, inputs, selected_candidate, candidate_id, session_id),
            },
        )

    def execute(self, context: ExecutionContext, inputs: Dict[str, Any]) -> CapabilityResult:
        selected_candidate = inputs.get("selectedCandidate") or {}
        session_id = inputs.get("ideaSessionId")
        candidate_id = inputs.get("selectedCandidateId")
        paper_type = inputs.get("paperType", "algorithm")
        title = selected_candidate.get("title") or inputs.get("seedQuery") or "FAROS Experiment"
        description = selected_candidate.get("problem") or inputs.get("notes") or "Experiment workspace generated by FAROS."
        language = inputs.get("language", "python")
        framework = inputs.get("framework", "FastAPI")
        return self._materialize_workspace(
            context=context,
            title=title,
            description=description,
            language=language,
            framework=framework,
            paper_type=paper_type,
            session_id=session_id,
            candidate_id=candidate_id,
            files=self._build_scaffold_files(title, paper_type, framework, language, inputs, selected_candidate, candidate_id, session_id),
            event_message=None,
        )

    def consume_provider_result(self, context: ExecutionContext, inputs: Dict[str, Any], provider_result: ProviderResult) -> CapabilityResult:
        payload = provider_result.payload
        selected_candidate = inputs.get("selectedCandidate") or {}
        session_id = inputs.get("ideaSessionId")
        candidate_id = inputs.get("selectedCandidateId")
        paper_type = inputs.get("paperType", "algorithm")
        title = selected_candidate.get("title") or inputs.get("seedQuery") or "FAROS Experiment"
        description = selected_candidate.get("problem") or inputs.get("notes") or "Experiment workspace generated by FAROS."
        return self._materialize_workspace(
            context=context,
            title=title,
            description=description,
            language=inputs.get("language", "python"),
            framework=inputs.get("framework", "FastAPI"),
            paper_type=paper_type,
            session_id=session_id,
            candidate_id=candidate_id,
            files=payload.get('files', []),
            event_message=provider_result.text,
            project_title_override=payload.get('projectTitle'),
        )

    def _materialize_workspace(self, context: ExecutionContext, title: str, description: str, language: str, framework: str, paper_type: str, session_id: str | None, candidate_id: str | None, files: List[Dict[str, str]], event_message: str | None, project_title_override: str | None = None) -> CapabilityResult:
        # Runtime-owned experiment execution must be able to provision its storage even
        # when the surrounding app lifecycle did not initialize the SQLModel tables yet.
        init_db()
        with get_session_context() as db:
            project = code_project_service.create_project(
                db=db,
                title=project_title_override or f"{title} [{language}]",
                description=description,
                language=language,
                framework=framework,
                source_idea_session_id=session_id,
                source_candidate_id=candidate_id,
            )
            code_project_service.write_project_files(db, project.id, files)
            project_id = project.id
            project_title = project.title

        experiment = create_experiment({
            "name": f"{title} Experiment",
            "projectId": project_id,
            "description": description,
            "tags": ["faros", "llm", paper_type],
            "status": "designed",
        })

        return CapabilityResult(
            status="completed",
            outputs={
                "projectId": project_id,
                "projectTitle": project_title,
                "experimentId": experiment["id"],
                "experimentStatus": experiment.get("status"),
                "experimentIds": [experiment["id"]],
            },
            artifacts=[
                ArtifactRecord(
                    id=f"{context.run_id}:{self.capability_id}:project",
                    type="code_project",
                    uri=f"project://{project_id}",
                    producer=self.capability_id,
                    summary=f"Project {project_id} created for experiment stage",
                    metadata={"projectId": project_id, "language": language, "framework": framework},
                ),
                ArtifactRecord(
                    id=f"{context.run_id}:{self.capability_id}:experiment",
                    type="experiment_record",
                    uri=f"experiment://{experiment['id']}",
                    producer=self.capability_id,
                    summary=f"Experiment {experiment['id']} linked to project {project_id}",
                    metadata={"experimentId": experiment['id'], "projectId": project_id},
                ),
            ],
            events=[
                {"level": "info", "message": event_message or f"Experiment stage provisioned project {project.id} and experiment {experiment['id']}"}
            ],
        )
